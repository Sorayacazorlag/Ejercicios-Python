#Preguntar a la usuaria cuántas horas trabaja
#Preguntar a cuánto cobra la hora
#Mostrar paga correspondiente

horas_trabajadas = float(input("¿Cuántas horas ha trabajado?: "))

cobro_hora = float(input("¿A cuánto cobra la hora?: "))

salario_total = horas_trabajadas * cobro_hora

print("Salario total: ", salario_total)

